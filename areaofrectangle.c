#include<stdio.h>

 
int main() {
   float length, width, area;
 
   printf("\nEnter the Length of Rectangle : ");
   scanf("%f", &length);
 
   printf("\nEnter the Width of Rectangle : ");
   scanf("%f", &width);
 
   area = length * width;
   printf("\nArea of Rectangle : %f\n", area);
 
   return (0);
}










